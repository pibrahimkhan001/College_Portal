<pre style="padding-left: 30px;"><span style="color: #3366ff;"><strong>Minutes of the Meeting:</strong></span></pre>
<p style="padding-left: 60px;">1. Prof. K. Prahlada Rao, Principal, Member &amp; Convener welcomed the members of Governing body.</p>
<p style="padding-left: 60px;">2. Sri. J. A. Chowdary, Chairman initiated the meeting with his opening remarks which covered the vision that Institution should have.</p>
<p style="padding-left: 60px;">        He emphasized the current concepts namely back to basics, ekalavya education, disruption in thinking need to be incorporated in
education. He directed all the members to contribute towards the development of the Institution.</p>
<p style="padding-left: 60px;">3. The Governing body unanimously confirmed the minutes of Ist Governing body meeting held on 23-01-2015.</p>
<p style="padding-left: 60px;">4. The Principal has presented that J.N.T.U.A. College of Engineering, Ananthapuramu has been sanctioned an amount of Rs. 7 crores from
Government of India under TEQIP –III in the subcomponent 1.3.</p>
<p style="padding-left: 60px;">5. The Governing body discussed and approved the bye-laws &amp; the following allowances for Governing body meetings.</p>
<p style="padding-left: 60px;">The proposed remuneration and TA/DA for the Governing Body is as follows:</p>
<ul>
<li style="padding-left: 90px;">Sitting allowance of Rs.5, 000/- per sitting</li>
<li style="padding-left: 90px;">Local conveyance by a Car</li>
<li style="padding-left: 90px;">Actual boarding &amp; lodging expenditure</li>
<li style="padding-left: 90px;">TA &amp; DA as per A.P. TA/DA rules</li>
<li style="padding-left: 90px;">However for the Chairman/external Member may be permitted to travel by the Air/Car, wherever applicable, shall be permitted.</li>
</ul>
<p style="padding-left: 60px;">6. The Governing body has ratified the action of the Principal for the appointment of the following staff members to look after the various
activities under TEQIP-III project (Annexure I)<br />
• Dr. D. Vishnu Vardhan, Coordinator, TEQIP-III<br />
• Prof. M. L. S. Deva Kumar, Nodal Officer, Academics, TEQIP-III<br />
• Dr. A. P. Siva Kumar, Nodal Officer, Procurement, TEQIP-III<br />
• Dr. K. Jithendra Gowd, Nodal Officer, Finance, TEQIP-III</p>
<p style="padding-left: 60px;">7. The Governing body has ratified the action of the Principal for the appointment of the following purchase Committees at Department
Level and Institution level under TEQIP-III project (Annexure II). The HODs will act as convener for the Department level purchase
committee and TEQIP Co-ordinator will act as convener for the Centralized purchase committee.</p>
<p style="padding-left: 60px;">8. The Governing body has approved and permitted to recruit additional Staff for TEQIP- III. Only Three staff members are permitted for
appointment. i.e.</p>
<p style="padding-left: 60px;">1. Accounts and officer in-charge &#8211; Rs. 20,000/- per month<br />
2. MIS officer &#8211; Rs. 20,000/- per month<br />
3. Office subordinate &#8211; Rs. 12,000/- per month</p>
<p style="padding-left: 60px;">For paying the salaries to the above three staff, appointment orders are to be registered in advance with PFMS Web Portal.</p>
<p style="padding-left: 60px;">9. The Governing body has ratified the submitted action plans for Quarter 3 and Quarter 4 to NPIU (Annexure &#8211; III).
10. The Governing body has ratified the guidelines for organizing FDPs /conferences / seminars / workshops based on TEQIP-III guidelines<
(Annexure – IV)</p>
<p style="padding-left: 60px;">11. The Governing body has ratified the norms for organizing short term training programmes / workshops for students (Annexure – V).</p>
<p style="padding-left: 60px;">12. The Governing body has ratified for sanctioning the Seed grant to faculty members / students to venture into innovative research /
R&amp;D activities (Annexure &#8211; VI).</p>
<p style="padding-left: 60px;">13. The Governing body has ratified for Organizing remedial /preparatory classes to increase transition rate.
It is proposed to provide academic support for weak students to improve the student’s transition from first year to second year &amp;
second year to third year etc. of UG programs by conducting remedial classes after the college hours. The teachers may be drawn from
college faculty, faculty from outside or meritorious senior students.</p>
<p style="padding-left: 90px;">They may be paid the following remunerations:</p>
<p style="padding-left: 120px;">Remuneration of Rs.500/- per session of 1hr for outside faculty only.
TA &amp; DA will be given only for out station persons as per A.P. TA/DA rules on production of original tickets.</p>
<p style="padding-left: 60px;">14.The Governing body has approved the TA/DA and Accommodation charges as below:</p>
<p style="padding-left: 90px;">i. For Regular Faculty – As per NPIU TA/DA Guidelines (Annexure- VII). In addition to that, two way car fare shall be permitted either to drop and/or pick-up to/from Airport.</p>
<p style="padding-left: 90px;">ii. For Non Teaching Staff &#8211; TA &#8211; train (Third A.C) /Bus, DA- Rs. 400/- per day (within the state), Rs. 500/- (outside the state) and accommodation- actuals / limited to Rs.1,000/- per day within the state and Rs.1500/- for outside the state; Registration fee &#8211; actuals / limited to Rs. 10,000/-.</p>
<p style="padding-left: 90px;">iii. For Adhoc Lectures &#8211; TA &#8211; train (Third A.C) /Bus, DA- Rs.200/- per day (within the state), Rs. 300/- (outside the state) and accommodation-actuals / limited to Rs.1,000/- per day within the state and Rs.1500/- for outside the state; Registration fee &#8211; actuals / limited to Rs. 10,000/-</p>
<p style="padding-left: 90px;">iv. For Scholars/Students &#8211; TA &#8211; Train (sleeper class)/Bus (super luxury), DA-Rs. 100/- per day (within the state), Rs. 200/-(outside the state); Accommodation &#8211; actuals / limited to Rs. 500/- per day; Registration fee-actual/limited to Rs. 8,000/-</p>
<p style="padding-left: 90px;">v. For Students attending internships in industries / reputed organizations -Travelling allowance-Train (sleeper class)/Bus (super luxury), DA-Rs. 100/- per day (withinthe state), Rs. 200/- (outside the state); Registration fee, if any &#8211; Rs.5,000/-</p>
<p style="padding-left: 60px;">15.The Governing body has considered and approved the norms for foreign travel utilization(Received from NPIU, TEQIP-III Norms) (Annexure &#8211; VIII).</p>
<p style="padding-left: 60px;">16. The Governing body has considered and approved to admit Ph.D. fulltime scholars and to pay stipend from the TEQIP grants as per the MHRD/UGC guidelines (Annexure &#8211; IX)</p>
<p style="padding-left: 60px;">17.The Governing body has considered and approved the proposed remuneration and TA/DA for Performance Auditors &amp; Officials from
SPFU/NPIU.</p>
<p style="padding-left: 90px;">• Sitting allowance of Rs.3,000/- per day.<br />
• Local conveyance by permitting Car.<br />
• Actual boarding &amp; lodging expenditure.<br />
• Members who are drawing grade pay 6000 and above are entitled to travel for meetings by Economic class Air India as per their entitlement. However, on non availability of ticket in Air India, Private Airlines by economy class may be allowed subject to the condition that the fare charged is not more than the Air India fare (domestic) and Air travel should not be permitted as a matter of course and to the extent<br />
possible, it should be performed by train<br />
• The members may be allowed to travel by 2nd Class AC train,in case of places not connected by rail, travel by AC bus for all those entitled and travel by any means of public transport is allowed provided total fare does not exceed the train fare and the mileage allowance for road journey performed in own car/taxi is Rs.16/- per km.</p>
<p style="padding-left: 60px;">18.The Governing body has ratified the starting of new PG Courses in the college w.e.f. the academic year 2016-17.<br />
S.No                                        Name of the Course                                                                                Department</p>
<p style="padding-left: 60px;">1              M.Tech – Advanced Manufacturing Systems(Self Finance)                                      Mechanical Engineering<br />
2              M.Tech – Quality Engineering and Management (Self Finance)                             Mechanical Engineering<br />
3              M.Tech – VLSI System Design (Self Finance)                                                              Electronics &amp;Communication Engineering<br />
4              M.Tech – Embedded Systems (Self Finance)                                                               Electronics &amp; Communication Engineering<br />
5              M.Tech – Computer Science &amp; Engineering (Self Finance)                                      Computer Science &amp; Engineering<br />
6              M.Tech – Environmental Engineering (Self Finance)                                                Chemical Engineering</p><br>
<h5><span style="color: #3366ff;"><strong>The following resolutions after discussing certain items with the permission of chair:</strong></span></h5><br>
<p style="padding-left: 30px;">i. The Governing body has resolved to represent the University to create programme for including research scholars for Ph.D programmes into<br />
various Engineering disciplines on fulltime/part-time basis for the mentee institution.<br />
ii. The Governing body has resolved to register the faculty for SWAYAM courses and initiate steps for infrastructure set up for Swayam Prabha.<br />
iii. The Governing body has resolved to setup community radio in the campus.<br />
iv. The Governing body has resolved to initiate steps to start PG level programme in the area of Advanced Mathematics and Algorithms.<br />
v. The Governing body has resolved to conduct workshop and an international conference in Advanced Mathematics and Algorithms.<br />
vi. The Governing body has resolved to conduct Training programmes as per the industry requirements.<br />
vii. The Governing body has resolved to start an open house in collaboration with IBM.<br />
viii. The Governing body has resolved to conduct various skill development / entrepreneurship skills/employable skills programmes to students<br />
and make them employable.<br />
ix. The Governing body has resolved to initiate the ground work for the establishment of MOOCS University, a unique concept first of its kind<br />
in the history of Indian education.</p>




