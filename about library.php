<ul>
<li><strong>Introduction of the Library</strong></li>
<li>The Central Library supports the teaching and research programmers of the Institute and provides facilities for general reading and disseminates information according to the requirement of the users. It is housed in a separate building with a carpet area of 1006.32 Sq.ms, reading space 504.68 sq. ms.. It has around 85790 including, books, back volumes of technical national and international journals, technical pamphlets, standards, CD-ROMs, Video cassettes etc.  The database of entire library acquisitions was created using Library Application Software Called   KOHA   software.</li>
<li>The College   Central Library caters to the needs of the user community i.e., UG and PG students, Research scholars, staff and faculty of the Jawaharlal Nehru technological University college of Engineering (Autonomous), Ananthapuramu. The College Central Library was fully automated with bar coding of the books from the year 2011   KOHA   Library Software package. The main features of the KOHA   Software are OPAC, Acquisition, Cataloguing, Circulation, Serial Control etc.</li></ul>
