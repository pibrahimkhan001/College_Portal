<h3>About NSS</h3>
<p>Popularly known as NSS, the scheme was launched in Gandhiji&#8217;s Centenary year, 1969. Aimed at developing student&#8217;s personality through community service. NSS, is a voluntary association of young people in Colleges, Universities and at +2 level working for a campus-community linkage.</p>
<p>The cardinal principle of the NSS programme is that it is organised by the students themselves, and both students and teachers through their combined participation in community service, get a sense of involvement in the tasks of nation building.</p>
<p>Objectives behind the NSS:</p>
<ul>
<li>To enable the students to understand the community in which they work.</li>
<li>To understand themselves in relation to their community.</li>
<li>To identify the needs and problems in the community in the solution of which they can be involved.</li>
<li>To develop among themselves a sense of social and civic responsibility.</li>
<li>To apply education- find practical solution to individual &amp; community problems.</li>
<li>To develop competence required for group living and sharing responsibilities.</li>
<li>Gain skills in mobilising community participation.</li>
<li>To acquire leadership qualities and democratic attitude.</li>
<li>To develop capacity to meet emergencies and national disasters</li>
<li>To practice national integration.</li>
</ul>
<p>&nbsp;</p>
<p>NSS Motto:</p>
<p>The motto or watchword of the National Service Scheme is &#8216;NOT ME BUT YOU&#8217;. This reflects the essence of democratic living and upholds the need for selfless service and appreciation of the other person&#8217;s point of view and also to show consideration for fellow human beings. It underlines that the welfare of an individual is ultimately dependent on the welfare of society on the whole. Therefore, it should be the aim of the NSS to demonstrate this motto in its day-to-day programme.</p>
<p>NSS Day:</p>
<p>NSS was formally launched on 24th September, 1969, the birth centenary year of the Father of the Nation. Therefore, 24 September is celebrated every year as NSS Day with appropriate programmes and activities.</p>
