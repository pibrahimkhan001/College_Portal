            <p style="text-align: center;"><span style="color: #0000ff;"><strong>Entrepreneurship Development Programming Cell, JNTUA CEA</strong></span></p>
<p><span style="color: #ff0000;"><strong>     General Information:</strong></span></p>
<p>The EDP Cell, JNTUA CEA has been established in 2008 to promote the idea of Entrepreneurship amongst the           students of the College as this is very much essential for the economic growth of the country.</p>
<p><strong>     <span style="color: #ff0000;">Objectives:</span></strong></p>
<ul>
<li style="padding-left: 60px;">Since its inception the Cell has strived to imbibe the students with the skills and qualities required to become good entrepreneurs.</li>
<li style="padding-left: 60px;">The Cell has been encouraging the innovative ideas of students by providing technical support and introducing them to the appropriate change agents for further related interaction.</li>
</ul>
<p><span style="color: #ff0000;"><strong>      Functions:</strong></span></p>
<ul>
<li style="padding-left: 60px; text-align: justify;">The Cell is an effective platform for the students to develop the entrepreneurial attitude as it arranges        several Seminars and Workshops, some sponsored by DST, UCG etc., through which they provided with an          opportunity to meet eminent Entrepreneurs and Government Officials</li>
<li style="padding-left: 60px; text-align: justify;">Alumni , who have established their own Enterprise are invited to share their experiences with the students regularly.</li>
<li style="padding-left: 60px; text-align: justify;">Industrial visits are arranged for the Staff and Students.</li>
<li style="padding-left: 60px; text-align: justify;">Faculty Development Programmes are organized for the College Staff to inspire the students to become the leaders in the society by inculcating the entrepreneurial spirit in them.</li>
</ul>
