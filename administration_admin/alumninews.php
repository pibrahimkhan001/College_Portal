<p>&nbsp;&nbsp;Union Government has appointed renowned scientist Nageshwara Rao Guntur as Chairperson of Atomic Energy Regulatory Board (AERB). His appointment as the AERB chairperson will be for three years.  Nageshwara Rao Guntur is the chairman, Project Design Safety Committee, Prototype Fast Breeder Reactor (PFBR) and former distinguished scientist Nuclear Power Corporation of India Ltd (NPCIL).

Atomic Energy Regulatory Board (AERB) was established in November 1983 to carry out certain regulatory and safety functions in the fields of nuclear and radiation safety on a countrywide basis. It was constituted by President of India by exercising powers conferred by Section 27 of Atomic Energy Act, 1962 to carry out certain regulatory and safety functions under the Act. The regulatory authority of AERB is derived from rules and notifications promulgated under Atomic Energy Act, 1962 and Environmental (Protection) Act, 1986. It is headquartered in Mumbai, Maharashtra. Its mission is to ensure that use of ionising radiation and nuclear energy in India does not cause undue risk to health and environment. Currently, it consists of full-time Chairman, an ex officio Member, three part-time Members and Secretary.</p>

<p>&nbsp;&nbsp;న్యూక్లియర్‌ పవర్‌ కార్పొరేషన్‌లో పని చేసిన కాలంలో నాగేశ్వరరావు ప్రతిభ జాతీయ, అంతర్జాతీయ స్థాయిలో గుర్తింపు పొందింది. అణుశక్తి నియంత్రణ మండలిలో ప్రాజెక్టు డిజైన్‌ సేఫ్టీ కమిటీ చైర్మన్‌గా ప్రస్తుతం నాగేశ్వరరావు కొనసాగుతున్నారు. ప్రొటోటైప్‌ ఫాస్ట్‌ బ్రీడర్‌ రియాక్టర్‌ ప్రాజెక్టులకు ఆయన నాయకత్వం వహిస్తున్నారు.
న్యూక్లియర్‌ రంగంలో సీనియర్‌ శాస్త్రవేత్త అయిన నాగేశ్వరరావు గుంటూరు జిల్లా చిలువూరు గ్రామంలో జన్మించారు. అనంతపురం ప్రభుత్వ ఇంజనీరింగ్‌ కాలేజీలో ఎలక్ట్రికల్‌ ఇంజనీరింగ్‌ పూర్తి చేశారు.
1975లో భారత అణు ఇంధన విభాగంలో చేరారు. ‘వరల్డ్‌ అసోసియేషన్‌ ఆఫ్‌ న్యూక్లియర్‌ ఆపరేషన్స్‌ (వానో) ఆయనకు ‘న్యూక్లియర్‌ ఎక్స్‌లెన్స్‌’ పురస్కారం అందించింది.
సురక్షిత, సమర్థ, విశ్వసనీయ అణు విద్యుత్తు కేంద్రా నిర్వహణకు సంబంధించిన సాంకేతిక పరిజ్ఞానం, అనుభవానికి గుర్తింపుగా ఆయనకు ఈ అవార్డు లభించింది.</p>
