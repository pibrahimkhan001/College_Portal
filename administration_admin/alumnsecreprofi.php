<p><strong>The following are the achievements: </strong></p>
<ol>
<li>Development of various labs in EEE Department viz., programming in ‘C’, Numerical methods, Data structures, Networks, Machines labs.</li>
<li>Development of and introduction of new subjects at UG/PG level in the area and Reliability Engineering in EEE Department and instrumental in deciding course structure and syllabus in this area and applications to power systems.</li>
<li>Served as Chairman Board of Studies of JNTU Hyderabad at JNTUCE(A), Anantapur and presently member</li>
<li>Member of various Inspection Committees for JNTU, AICTE and staff selection committees at various colleges in Southern region.</li>
<li>TAPTEC scheme under AICTE got sanctioned for Rs.5 lakhs and completed.</li>
<li>Referee of “IEEE Transactions on Reliability” an International Journal</li>
<li>Received AP State Best Teacher Award on 5<sup>th</sup> September 2010.</li>
<li>Served as Director i/c of Foreign Affairs &amp; Alumni Matters from 9<sup>th</sup> September 2010 to 07.12.2010.</li>
<li>Elected as Fellow of Institute of Engineers (India) – FIE, w. e. f 13-12-2010, No. F – 115533 – 0.</li>
<li>He is a Senior Member of IEEE, Member No. 90339465.</li>
<li>He is a Life Member of ISTE, ISPE (LM-2532), SSI and SESI (LM/1887/2012).</li>
<li>He is instrumental starting the M. Tech. in Reliability Engineering in the University during the academic year 2009-10, which is an interdisciplinary area.</li>
<li>He is a member of the committee as Outstanding Engineering Personality to Co-opt – IEI, Kadapa Local Center for the years 2010-11 &amp; 2011-12.</li>
<li>Received Rashtriya Gaurav Award for outstanding service achievements and contributions on 16<sup>th</sup> July 2011</li>
<li>Received a life time achievement award “Rayalaseema Vidhya Rathna” from Swami art creations, Proddatur on 16<sup>th</sup> 2012.</li>
<li>He is a Member of International Association of Engineers (IAENG), Member No. 121481.</li>
<li>He has got RCP (Research Contingency Project) Rs. 5.6 Lakhs as Consultant for the project on “Improvement in Reliability for Power Distribution Systems” for which MoU is signed with Registrar, JNTUA and Joint Director, CPRI Bangalore and completed.</li>
<li>He was faculty counselor of IEEE Student Branch of JNTUA CEA for the year 2013.</li>
<li>He has been appointed as Nodal Officer for JNTUA-NBA Nodal Centre from April 2013-2015</li>
<li>He is a member of Academic Senate of Krishna University, Machilipatnam from Jan. 2014.</li>
<li>He is a Coordinator, Student Activities of IEEE, Ananthapuramu Zone from 04.04.2014 to 5<sup>th</sup> 2016.</li>
<li>He has served as Chief Coordinator, Spoken Tutorial Project, IIT Mumbai – JNTUA from July 2014 to 28<sup>th</sup> January 2015.</li>
<li>He is an Executive Group Member of AP Knowledge Mission from Feb. 2015.</li>
<li>He is chief coordinator for Centers of Excellence of all Universities in A.P. since April 2015.</li>
<li>He is Chairman of IEEE Student Activities Center (SAC) of Ananthapuramu Zone of Andhra Pradesh from 6<sup>th</sup> January-2016 to 16<sup>th</sup> Feb-2017.</li>
<li>He is Coordinator of RUSA at JNTUA from 19<sup>th</sup> January-2016.</li>
<li>Member of Indian Wind Power Association, since April 2016.</li>
<li>He is a recipient of Eminent Educationist Award by INDUS foundation during INDO- AMERICAN education summit conducted on 23.09.2016 at Bangalore, India.</li>
<li>He is the Chief Editor of Journal of Power Systems published by i-Manager publications Nagercoil.</li>
<li>He is Chairman of Research Advisory Committee in Electrical Engg. of JNTUA since Jan-2017.</li>
